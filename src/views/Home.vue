const statNumber = ref([])
const animateNumber = (el, target) => {
  let current = 0
  const step = Math.ceil(target / 50)
  const timer = setInterval(() => {
    current += step
    if (current >= target) {
      current = target
      clearInterval(timer)
    }
    el.textContent = current.toLocaleString()
